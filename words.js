window.words.forEach(word => {
    // اضافه کردن سطح دشواری
    if (!word.level) {
        const length = word.english.length;
        if (length <= 4) word.level = 'آسان';
        else if (length <= 7) word.level = 'متوسط';
        else word.level = 'دشوار';
    }
    
    // اضافه کردن دسته‌بندی
    if (!word.category) {
        // تعیین دسته‌بندی بر اساس معنی
        if (word.english.includes('ly') && word.definition.includes('manner')) {
            word.category = 'قید';
        } else if (['happy', 'sad', 'angry'].includes(word.english)) {
            word.category = 'احساسات';
        } else if (['red', 'blue', 'green'].includes(word.english)) {
            word.category = 'رنگ‌ها';
        } else if (['big', 'small', 'tall'].includes(word.english)) {
            word.category = 'صفات';
        } else {
            word.category = 'عمومی';
        }
    }
    
    // اضافه کردن تاریخ یادگیری
    if (!word.learnedDate) {
        word.learnedDate = null;
    }
    
    // اضافه کردن تعداد مرورها
    if (!word.reviewCount) {
        word.reviewCount = 0;
    }
    
    // اضافه کردن درصد یادگیری
    if (!word.mastery) {
        word.mastery = 0;
    }
});

